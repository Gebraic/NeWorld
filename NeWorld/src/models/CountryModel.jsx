export class Country {
    constructor(name, capital, region, population, flags, languages, timezones, maps, currencies) {
        this.name = name;
        this.capital = capital;
        this.region = region;
        this.population = population;
        this.flags = flags;
        this.languages = languages;
        this.timezones = timezones;
        this.maps = maps;
        this.currencies = currencies;
    }
}